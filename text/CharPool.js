
/**
 * 常用字符常量
 */
export default class CharPool {
	/**
	 * 字符常量：空格符 {@code ' '}
	 */
	static SPACE = ' ';

	/**
	 * 字符常量：制表符 {@code '\t'}
	 */
	static  TAB = '	';

	/**
	 * 字符常量：点 {@code '.'}
	 */
	static  DOT = '.';

	/**
	 * 字符常量：斜杠 {@code '/'}
	 */
	static  SLASH = '/';

	/**
	 * 字符常量：反斜杠 {@code '\\'}
	 */
	static  BACKSLASH = '\\';

	/**
	 * 字符常量：回车符 {@code '\r'}
	 */
	static  CR = '\r';

	/**
	 * 字符常量：换行符 {@code '\n'}
	 */
	static  LF = '\n';

	/**
	 * 字符常量：减号（连接符） {@code '-'}
	 */
	static  DASHED = '-';

	/**
	 * 字符常量：下划线 {@code '_'}
	 */
	static  UNDERLINE = '_';

	/**
	 * 字符常量：逗号 {@code ','}
	 */
	static  COMMA = ',';

	/**
	 * 字符常量：花括号（左） <code>'{'</code>
	 */
	static  DELIM_START = '{';

	/**
	 * 字符常量：花括号（右） <code>'}'</code>
	 */
	static  DELIM_END = '}';

	/**
	 * 字符常量：中括号（左） {@code '['}
	 */
	static  BRACKET_START = '[';

	/**
	 * 字符常量：中括号（右） {@code ']'}
	 */
	static  BRACKET_END = ']';

	/**
	 * 字符常量：双引号 {@code '"'}
	 */
	static  DOUBLE_QUOTES = '"';

	/**
	 * 字符常量：单引号 {@code '\''}
	 */
	static  SINGLE_QUOTE = '\'';

	/**
	 * 字符常量：与 {@code '&'}
	 */
	static  AMP = '&';

	/**
	 * 字符常量：冒号 {@code ':'}
	 */
	static  COLON = ':';

	/**
	 * 字符常量：艾特 {@code '@'}
	 */
	static  AT = '@';
}
